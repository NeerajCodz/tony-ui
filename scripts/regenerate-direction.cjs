const fs = require('fs');
const path = require('path');

const COMPONENT_NAME = 'direction';
const VERSIONS = [
  'angular-corner',
  'holo-frame',
  'data-panel',
  'circuit-board',
  'quantum-gate',
  'tactical-hud',
  'energy-shield',
  'terminal-window',
  'matrix-grid',
  'neon-outline'
];

const TARGET_DIR = path.join(__dirname, `../src/ui/components/${COMPONENT_NAME}`);

if (!fs.existsSync(TARGET_DIR)) {
  fs.mkdirSync(TARGET_DIR, { recursive: true });
}

// Helper to generate the component file content
const TEMPLATE = (version) => {
  let clipPath = 'none';
  let borderStyle = '';
  
  switch (version) {
    case 'angular-corner':
      clipPath = 'polygon(10px 0, 100% 0, 100% calc(100% - 10px), calc(100% - 10px) 100%, 0 100%, 0 10px)';
      break;
    default:
      clipPath = 'inset(0 0 0 0 round 4px)';
  }

  return `
import * as React from "react"
import { ArrowRight, ArrowLeft, ArrowUp, ArrowDown, Compass } from "lucide-react"
import { cn } from "../../utils/component-helpers"

interface DirectionProps extends React.HTMLAttributes<HTMLDivElement> {
  direction?: 'up' | 'down' | 'left' | 'right' | 'ne' | 'nw' | 'se' | 'sw'
  size?: 'sm' | 'md' | 'lg'
  version?: string
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info'
  type?: 'default' | 'outline' | 'solid' | 'ghost'
  animated?: boolean
}

const Component = React.forwardRef<HTMLDivElement, DirectionProps>(({
  direction = 'right',
  size = 'md',
  className,
  version = '${version}',
  variant = 'primary',
  type = 'default',
  animated = true,
  children,
  ...props
}, ref) => {
  
  const colorMap: Record<string, string> = {
    neutral: 'primary', success: 'success', warning: 'warning', info: 'info', destructive: 'destructive', primary: 'primary'
  };
  const color = colorMap[variant] || 'primary';

  const getTypeStyles = () => {
    const baseColor = \`hsl(var(--\${color}-base))\`;
    switch(type) {
      case 'outline': return { border: \`1px solid \${baseColor}\`, background: 'transparent' };
      case 'solid': return { background: \`hsl(var(--\${color}-base) / 0.1)\`, border: 'none' };
      case 'ghost': return { background: 'transparent', border: 'none' };
      default: return { 
        background: \`hsl(var(--\${color}-base) / 0.05)\`, 
        border: \`1px solid hsl(var(--\${color}-base) / 0.3)\` 
      };
    }
  };

  const styles = getTypeStyles();
  
  const getIcon = () => {
    switch(direction) {
      case 'up': return <ArrowUp className="w-full h-full" />;
      case 'down': return <ArrowDown className="w-full h-full" />;
      case 'left': return <ArrowLeft className="w-full h-full" />;
      case 'right': return <ArrowRight className="w-full h-full" />;
      default: return <Compass className="w-full h-full" />;
    }
  };
  
  const sizeMap = {
    sm: 'w-8 h-8',
    md: 'w-12 h-12',
    lg: 'w-16 h-16'
  };

  return (
    <div
      ref={ref}
      className={cn(
        "relative flex items-center justify-center transition-all duration-300",
        sizeMap[size],
        animated && "hover:scale-110",
        className
      )}
      style={{
        ...styles,
        clipPath: '${clipPath}',
        color: \`hsl(var(--\${color}-foreground))\`
      }}
      {...props}
    >
      <div className={cn("transition-transform duration-500", animated && "group-hover:rotate-45")}>
        {children || getIcon()}
      </div>
      
      {/* Decorative Elements */}
      <div className="absolute inset-0 pointer-events-none opacity-50" 
           style={{ border: \`1px dashed hsl(var(--\${color}-base) / 0.3)\`, clipPath: '${clipPath}' }} />
    </div>
  )
})

Component.displayName = "Direction-${version}"

export default Component
`;
};

// Generate each version
VERSIONS.forEach(version => {
  const content = TEMPLATE(version);
  const filePath = path.join(TARGET_DIR, `${COMPONENT_NAME}-${version}.tsx`);
  fs.writeFileSync(filePath, content);
  console.log(`Generated ${COMPONENT_NAME}-${version}.tsx`);
});

// Generate index.tsx
const indexContent = `/**
 * Direction Component - Dynamic Renderer
 * Generated by script
 */

import React, { lazy, Suspense } from 'react';

// Fallback types if file doesn't exist
type DirectionVersion = string;
interface DirectionProps extends React.HTMLAttributes<HTMLDivElement> {
  version?: DirectionVersion;
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info';
  type?: 'default' | 'outline' | 'solid' | 'ghost';
  direction?: 'up' | 'down' | 'left' | 'right' | 'ne' | 'nw' | 'se' | 'sw';
  size?: 'sm' | 'md' | 'lg';
  [key: string]: any;
}

// Config import
let componentConfig: any = { versions: {} };
try {
  // @ts-ignore
  // componentConfig = require('../../config/components/${COMPONENT_NAME}.json');
} catch (e) {
  // console.warn('Config not found for ${COMPONENT_NAME}');
}

// Dynamic imports for versions
const versionComponents: Record<string, any> = {
${VERSIONS.map(v => `  '${v}': lazy(() => import('./${COMPONENT_NAME}-${v}')),`).join('\n')}
};

// Loading Skeleton
const LoadingSkeleton: React.FC = () => (
  <div className="animate-pulse bg-muted/20 w-12 h-12 rounded" />
);

// Fallback Component
const FallbackComponent = React.forwardRef<HTMLElement, DirectionProps>(({ 
  children, 
  className = '',
  version,
  ...props 
}, ref) => (
  <div ref={ref as any} className={\`p-4 border border-dashed border-red-500/50 bg-red-500/10 \${className}\`} {...props}>
    <span className="text-xs text-red-400 font-mono">Missing: {version}</span>
    {children}
  </div>
));
FallbackComponent.displayName = 'FallbackDirection';

// Main Component
const DirectionBase = React.forwardRef<HTMLElement, DirectionProps>(({ 
  version = 'angular-corner',
  ...props 
}, ref) => {
  const VersionComponent = versionComponents[version];
  
  if (!VersionComponent) {
    return <FallbackComponent ref={ref} version={version} {...props} />;
  }

  return (
    <Suspense fallback={<LoadingSkeleton />}>
      <VersionComponent ref={ref} {...props} />
    </Suspense>
  );
});
DirectionBase.displayName = 'Direction';

export const Direction = DirectionBase;

export default Direction;
`;

fs.writeFileSync(path.join(TARGET_DIR, 'index.tsx'), indexContent);
console.log('Generated index.tsx');

// Update main index.ts export
const mainIndexInitPath = path.join(__dirname, '../src/ui/index.ts');
let mainIndexContent = fs.readFileSync(mainIndexInitPath, 'utf8');

// Check if already exported
if (!mainIndexContent.includes("export * from './components/direction'")) {
    // Add to components section
    const exportStatement = "export * from './components/direction';";
    // Find a good place to insert (e.g. after date-picker)
    if (mainIndexContent.includes("export * from './components/date-picker';")) {
        mainIndexContent = mainIndexContent.replace(
            "export * from './components/date-picker';",
            "export * from './components/date-picker';\n" + exportStatement
        );
    } else {
        // Append to end if specific place not found
        mainIndexContent += `\n${exportStatement}`;
    }
    fs.writeFileSync(mainIndexInitPath, mainIndexContent);
    console.log('Updated src/ui/index.ts with export');
}

console.log('Direction regeneration complete.');
