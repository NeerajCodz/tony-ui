const fs = require('fs');
const path = require('path');

const COMPONENT_NAME = 'input-otp';
const VERSIONS = [
  'angular-corner',
  'holo-frame',
  'data-panel',
  'circuit-board',
  'quantum-gate',
  'tactical-hud',
  'energy-shield',
  'terminal-window',
  'matrix-grid',
  'neon-outline'
];

const TARGET_DIR = path.join(__dirname, `../src/ui/components/${COMPONENT_NAME}`);

if (!fs.existsSync(TARGET_DIR)) {
  fs.mkdirSync(TARGET_DIR, { recursive: true });
}

// Helper to generate the component file content
const TEMPLATE = (version) => {
  let clipPath = 'none';
  
  switch (version) {
    case 'angular-corner':
      clipPath = 'polygon(4px 0, 100% 0, 100% calc(100% - 4px), calc(100% - 4px) 100%, 0 100%, 0 4px)';
      break;
    default:
      clipPath = 'inset(0 0 0 0 round 4px)';
  }

  return `
import * as React from "react"
import { OTPInput, SlotProps } from "input-otp"
import { cn } from "../../utils/component-helpers"

interface InputOtpProps {
  version?: string
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info'
  type?: 'default' | 'outline' | 'solid' | 'ghost'
  maxLength?: number
  value?: string
  onChange?: (value: string) => void
  className?: string
}

const Component = React.forwardRef<HTMLInputElement, InputOtpProps>(({
  version = '${version}',
  variant = 'primary',
  type = 'default',
  maxLength = 6,
  value,
  onChange,
  className,
  ...props
}, ref) => {
  
  const colorMap: Record<string, string> = {
    neutral: 'primary', success: 'success', warning: 'warning', info: 'info', destructive: 'destructive', primary: 'primary'
  };
  const color = colorMap[variant] || 'primary';

  const getTypeStyles = (isActive: boolean) => {
    const baseColor = \`hsl(var(--\${color}-base))\`;
    if (isActive) {
       return { 
         borderColor: baseColor,
         boxShadow: \`0 0 10px \${baseColor}\`,
         background: \`hsl(var(--\${color}-base) / 0.1)\`
       };
    }

    switch(type) {
      case 'outline': return { border: \`1px solid \${baseColor}\`, background: 'transparent' };
      case 'solid': return { background: \`hsl(var(--\${color}-base) / 0.1)\`, border: 'none' };
      default: return { 
        background: \`hsl(var(--\${color}-base) / 0.05)\`, 
        border: \`1px solid hsl(var(--\${color}-base) / 0.3)\` 
      };
    }
  };

  function Slot(props: SlotProps) {
    const styles = getTypeStyles(props.isActive);
    
    return (
      <div
        className={cn(
          "relative w-10 h-14 text-[2rem]",
          "flex items-center justify-center",
          "transition-all duration-300",
          "group-hover:border-accent-foreground/20 group-focus-within:border-accent-foreground/20",
          "outline outline-0 outline-accent-foreground/20",
          { "outline-4 outline-accent-foreground": props.isActive },
        )}
        style={{
          ...styles,
           clipPath: '${clipPath}',
           fontFamily: 'var(--font-mono)',
           color: \`hsl(var(--\${color}-foreground))\`
        }}
      >
        {props.char !== null && <div>{props.char}</div>}
        {props.hasFakeCaret && (
          <div className="absolute pointer-events-none inset-0 flex items-center justify-center">
            <div className="w-px h-8 bg-white/70 animate-caret-blink" />
          </div>
        )}
      </div>
    )
  }

  function FakeDash() {
    return (
      <div className="flex w-10 justify-center items-center">
        <div className="w-3 h-1 rounded-full bg-border" style={{ background: \`hsl(var(--\${color}-base) / 0.3)\` }} />
      </div>
    )
  }

  return (
    <OTPInput
      ref={ref as any}
      maxLength={maxLength}
      value={value}
      onChange={onChange}
      containerClassName={cn("flex items-center gap-2 has-[:disabled]:opacity-30", className)}
      render={({ slots }) => (
        <>
          <div className="flex gap-2">
            {slots.slice(0, 3).map((slot, idx) => (
              <Slot key={idx} {...slot} />
            ))}
          </div>
          {maxLength > 3 && <FakeDash />}
          <div className="flex gap-2">
            {slots.slice(3).map((slot, idx) => (
              <Slot key={idx + 3} {...slot} />
            ))}
          </div>
        </>
      )}
      {...props}
    />
  )
})

Component.displayName = "InputOtp-${version}"

export default Component
`;
};

// Generate each version
VERSIONS.forEach(version => {
  const content = TEMPLATE(version);
  const filePath = path.join(TARGET_DIR, `${COMPONENT_NAME}-${version}.tsx`);
  fs.writeFileSync(filePath, content);
  console.log(`Generated ${COMPONENT_NAME}-${version}.tsx`);
});

// Generate index.tsx
const indexContent = `/**
 * InputOtp Component - Dynamic Renderer
 * Generated by script
 */

import React, { lazy, Suspense } from 'react';

// Fallback types if file doesn't exist
type InputOtpVersion = string;
interface InputOtpProps {
  version?: InputOtpVersion;
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info';
  type?: 'default' | 'outline' | 'solid' | 'ghost';
  maxLength?: number;
  value?: string;
  onChange?: (value: string) => void;
  [key: string]: any;
}

// Config import
let componentConfig: any = { versions: {} };
try {
  // @ts-ignore
  // componentConfig = require('../../config/components/${COMPONENT_NAME}.json');
} catch (e) {
  // console.warn('Config not found for ${COMPONENT_NAME}');
}

// Dynamic imports for versions
const versionComponents: Record<string, any> = {
${VERSIONS.map(v => `  '${v}': lazy(() => import('./${COMPONENT_NAME}-${v}')),`).join('\n')}
};

// Loading Skeleton
const LoadingSkeleton: React.FC = () => (
  <div className="animate-pulse bg-muted/20 w-full h-14 rounded" />
);

// Fallback Component
const FallbackComponent = React.forwardRef<HTMLElement, InputOtpProps>(({ 
  children, 
  className = '',
  version,
  ...props 
}, ref) => (
  <div ref={ref as any} className={\`p-4 border border-dashed border-red-500/50 bg-red-500/10 \${className}\`} {...props}>
    <span className="text-xs text-red-400 font-mono">Missing: {version}</span>
    {children}
  </div>
));
FallbackComponent.displayName = 'FallbackInputOtp';

// Main Component
const InputOtpBase = React.forwardRef<HTMLElement, InputOtpProps>(({ 
  version = 'angular-corner',
  ...props 
}, ref) => {
  const VersionComponent = versionComponents[version];
  
  if (!VersionComponent) {
    return <FallbackComponent ref={ref} version={version} {...props} />;
  }

  return (
    <Suspense fallback={<LoadingSkeleton />}>
      <VersionComponent ref={ref} {...props} />
    </Suspense>
  );
});
InputOtpBase.displayName = 'InputOtp';

export const InputOtp = InputOtpBase;

export default InputOtp;
`;

fs.writeFileSync(path.join(TARGET_DIR, 'index.tsx'), indexContent);
console.log('Generated index.tsx');

// Update main index.ts export
const mainIndexInitPath = path.join(__dirname, '../src/ui/index.ts');
let mainIndexContent = fs.readFileSync(mainIndexInitPath, 'utf8');

// Check if already exported
if (!mainIndexContent.includes("export * from './components/input-otp'")) {
    // Add to components section
    const exportStatement = "export * from './components/input-otp';";
    // Find a good place to insert (e.g. after input-group)
    if (mainIndexContent.includes("export * from './components/input-group';")) {
        mainIndexContent = mainIndexContent.replace(
            "export * from './components/input-group';",
            "export * from './components/input-group';\n" + exportStatement
        );
    } else {
        // Append to end if specific place not found
        mainIndexContent += `\n${exportStatement}`;
    }
    fs.writeFileSync(mainIndexInitPath, mainIndexContent);
    console.log('Updated src/ui/index.ts with export');
}

console.log('InputOtp regeneration complete.');
