const fs = require('fs');
const path = require('path');

const COMPONENT_NAME = 'hover-card';
const VERSIONS = [
  'angular-corner',
  'holo-frame',
  'data-panel',
  'circuit-board',
  'quantum-gate',
  'tactical-hud',
  'energy-shield',
  'terminal-window',
  'matrix-grid',
  'neon-outline'
];

const TARGET_DIR = path.join(__dirname, `../src/ui/components/${COMPONENT_NAME}`);

if (!fs.existsSync(TARGET_DIR)) {
  fs.mkdirSync(TARGET_DIR, { recursive: true });
}

// Helper to generate the component file content
const TEMPLATE = (version) => {
  let clipPath = 'none';
  
  switch (version) {
    case 'angular-corner':
      clipPath = 'polygon(10px 0, 100% 0, 100% calc(100% - 10px), calc(100% - 10px) 100%, 0 100%, 0 10px)';
      break;
    default:
      clipPath = 'inset(0 0 0 0 round 4px)';
  }

  return `
import * as React from "react"
import * as HoverCard from "@radix-ui/react-hover-card"
import { cn } from "../../utils/component-helpers"
import { Info, ExternalLink } from "lucide-react"

interface HoverCardProps {
  version?: string
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info'
  type?: 'default' | 'outline' | 'solid' | 'ghost'
  children?: React.ReactNode
  className?: string
  content?: React.ReactNode
}

const Component = React.forwardRef<HTMLDivElement, HoverCardProps>(({
  version = '${version}',
  variant = 'primary',
  type = 'default',
  children,
  className,
  content,
  ...props
}, ref) => {
  
  const colorMap: Record<string, string> = {
    neutral: 'primary', success: 'success', warning: 'warning', info: 'info', destructive: 'destructive', primary: 'primary'
  };
  const color = colorMap[variant] || 'primary';

  const getTypeStyles = () => {
    const baseColor = \`hsl(var(--\${color}-base))\`;
    switch(type) {
      case 'outline': return { border: \`1px solid \${baseColor}\`, background: 'black' };
      case 'solid': return { background: \`hsl(var(--\${color}-base) / 0.1)\`, border: 'none' };
      default: return { 
        background: 'rgba(10, 10, 15, 0.95)', 
        border: \`1px solid hsl(var(--\${color}-base) / 0.3)\`,
        backdropFilter: 'blur(8px)'
      };
    }
  };

  const styles = getTypeStyles();

  return (
    <HoverCard.Root>
      <HoverCard.Trigger asChild>
        {children || <span className="underline decoration-dashed decoration-gray-500 underline-offset-4 cursor-help text-gray-400 hover:text-white transition-colors">Hover me</span>}
      </HoverCard.Trigger>
      <HoverCard.Portal>
        <HoverCard.Content 
          className={cn(
            "w-[300px] z-50 rounded-md p-4 animate-in fade-in zoom-in-95 data-[side=bottom]:slide-in-from-top-2 data-[side=top]:slide-in-from-bottom-2",
            className
          )}
          style={{
            ...styles,
            clipPath: '${clipPath}',
            color: \`hsl(var(--\${color}-foreground))\`
          }}
          sideOffset={5}
        >
           {content || (
             <div className="flex flex-col gap-2">
               <div className="flex items-center gap-2 border-b border-white/10 pb-2 mb-1">
                 <div className="p-1 rounded bg-white/5 border border-white/10">
                   <Info className="h-4 w-4" style={{ color: \`hsl(var(--\${color}-base))\` }} />
                 </div>
                 <span className="font-mono text-sm font-bold tracking-tight uppercase" style={{ color: \`hsl(var(--\${color}-base))\` }}>
                   {version} INFO
                 </span>
               </div>
               <p className="text-sm text-gray-400 leading-relaxed">
                 Interactive data overlay providing contextual information about the target element. 
                 Optimized for high-density information displays.
               </p>
               <div className="flex items-center gap-2 mt-2 pt-2 border-t border-white/5">
                 <span className="text-xs text-gray-600 font-mono">ID: {Math.random().toString(36).substr(2, 6).toUpperCase()}</span>
                 <ExternalLink className="ml-auto h-3 w-3 text-gray-500" />
               </div>
             </div>
           )}
           <HoverCard.Arrow className="fill-current text-black opacity-90" style={{ fill: 'black' }} />
        </HoverCard.Content>
      </HoverCard.Portal>
    </HoverCard.Root>
  )
})

Component.displayName = "HoverCard-${version}"

export default Component
`;
};

// Generate each version
VERSIONS.forEach(version => {
  const content = TEMPLATE(version);
  const filePath = path.join(TARGET_DIR, `${COMPONENT_NAME}-${version}.tsx`);
  fs.writeFileSync(filePath, content);
  console.log(`Generated ${COMPONENT_NAME}-${version}.tsx`);
});

// Generate index.tsx
const indexContent = `/**
 * HoverCard Component - Dynamic Renderer
 * Generated by script
 */

import React, { lazy, Suspense } from 'react';

// Fallback types if file doesn't exist
type HoverCardVersion = string;
interface HoverCardProps {
  version?: HoverCardVersion;
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info';
  type?: 'default' | 'outline' | 'solid' | 'ghost';
  children?: React.ReactNode;
  content?: React.ReactNode;
  [key: string]: any;
}

// Config import
let componentConfig: any = { versions: {} };
try {
  // @ts-ignore
  // componentConfig = require('../../config/components/${COMPONENT_NAME}.json');
} catch (e) {
  // console.warn('Config not found for ${COMPONENT_NAME}');
}

// Dynamic imports for versions
const versionComponents: Record<string, any> = {
${VERSIONS.map(v => `  '${v}': lazy(() => import('./${COMPONENT_NAME}-${v}')),`).join('\n')}
};

// Loading Skeleton
const LoadingSkeleton: React.FC = () => (
  <div className="animate-pulse bg-muted/20 w-full h-10 rounded" />
);

// Fallback Component
const FallbackComponent = React.forwardRef<HTMLElement, HoverCardProps>(({ 
  children, 
  className = '',
  version,
  ...props 
}, ref) => (
  <div ref={ref as any} className={\`p-4 border border-dashed border-red-500/50 bg-red-500/10 \${className}\`} {...props}>
    <span className="text-xs text-red-400 font-mono">Missing: {version}</span>
    {children}
  </div>
));
FallbackComponent.displayName = 'FallbackHoverCard';

// Main Component
const HoverCardBase = React.forwardRef<HTMLElement, HoverCardProps>(({ 
  version = 'angular-corner',
  ...props 
}, ref) => {
  const VersionComponent = versionComponents[version];
  
  if (!VersionComponent) {
    return <FallbackComponent ref={ref} version={version} {...props} />;
  }

  return (
    <Suspense fallback={<LoadingSkeleton />}>
      <VersionComponent ref={ref} {...props} />
    </Suspense>
  );
});
HoverCardBase.displayName = 'HoverCard';

export const HoverCard = HoverCardBase;

export default HoverCard;
`;

fs.writeFileSync(path.join(TARGET_DIR, 'index.tsx'), indexContent);
console.log('Generated index.tsx');

// Update main index.ts export
const mainIndexInitPath = path.join(__dirname, '../src/ui/index.ts');
let mainIndexContent = fs.readFileSync(mainIndexInitPath, 'utf8');

// Check if already exported
if (!mainIndexContent.includes("export * from './components/hover-card'")) {
    // Add to components section
    const exportStatement = "export * from './components/hover-card';";
    // Find a good place to insert (e.g. after dropdown-menu)
    if (mainIndexContent.includes("export * from './components/dropdown-menu';")) {
        mainIndexContent = mainIndexContent.replace(
            "export * from './components/dropdown-menu';",
            "export * from './components/dropdown-menu';\n" + exportStatement
        );
    } else {
        // Append to end if specific place not found
        mainIndexContent += `\n${exportStatement}`;
    }
    fs.writeFileSync(mainIndexInitPath, mainIndexContent);
    console.log('Updated src/ui/index.ts with export');
}

console.log('HoverCard regeneration complete.');
