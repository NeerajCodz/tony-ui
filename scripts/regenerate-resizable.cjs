const fs = require('fs');
const path = require('path');

const COMPONENT_NAME = 'resizable';
const VERSIONS = [
  'angular-corner',
  'holo-frame',
  'data-panel',
  'circuit-board',
  'quantum-gate',
  'tactical-hud',
  'energy-shield',
  'terminal-window',
  'matrix-grid',
  'neon-outline'
];

const TARGET_DIR = path.join(__dirname, `../src/ui/components/${COMPONENT_NAME}`);

if (!fs.existsSync(TARGET_DIR)) {
  fs.mkdirSync(TARGET_DIR, { recursive: true });
}

// Helper to generate the component file content
const TEMPLATE = (version) => {
  let clipPath = 'none';
  
  switch (version) {
    case 'angular-corner':
      clipPath = 'polygon(10px 0, 100% 0, 100% calc(100% - 10px), calc(100% - 10px) 100%, 0 100%, 0 10px)';
      break;
    default:
      clipPath = 'inset(0 0 0 0 round 4px)';
  }

  return `
import * as React from "react"
import { Panel, Group as PanelGroup, Separator as PanelResizeHandle } from "react-resizable-panels"
import { cn } from "../../utils/component-helpers"
import { GripVertical } from "lucide-react"

interface ResizableProps {
  version?: string
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info'
  type?: 'default' | 'outline' | 'solid' | 'ghost'
  children?: React.ReactNode
  className?: string
  direction?: 'horizontal' | 'vertical'
}

const Component = React.forwardRef<HTMLDivElement, ResizableProps>(({
  version = '${version}',
  variant = 'primary',
  type = 'default',
  children,
  className,
  direction = 'horizontal',
  ...props
}, ref) => {
  
  const colorMap: Record<string, string> = {
    neutral: 'primary', success: 'success', warning: 'warning', info: 'info', destructive: 'destructive', primary: 'primary'
  };
  const color = colorMap[variant] || 'primary';

  const getTypeStyles = () => {
    const baseColor = \`hsl(var(--\${color}-base))\`;
    switch(type) {
      case 'outline': return { border: \`1px solid \${baseColor}\`, background: 'transparent' };
      case 'solid': return { background: \`hsl(var(--\${color}-base) / 0.1)\`, border: 'none' };
      default: return { 
        background: \`hsl(var(--\${color}-base) / 0.05)\`, 
        border: \`1px solid hsl(var(--\${color}-base) / 0.3)\` 
      };
    }
  };

  const styles = getTypeStyles();

  return (
    <div 
      className={cn("h-[200px] w-full rounded-md border", className)}
      style={{
         ...styles,
         borderColor: \`hsl(var(--\${color}-base) / 0.3)\`
      }}
    >
      <PanelGroup direction={direction} className="rounded-lg">
        {children || (
          <>
            <Panel defaultSize={50} className="flex items-center justify-center p-6">
              <span className="font-semibold" style={{ color: \`hsl(var(--\${color}-base))\` }}>One</span>
            </Panel>
            <PanelResizeHandle className="relative flex w-px items-center justify-center bg-border after:absolute after:inset-y-0 after:left-1/2 after:w-1 after:-translate-x-1/2 focus-visible:outline-none focus-visible:ring-1 focus-visible:ring-ring focus-visible:ring-offset-1">
               <div className="z-10 flex h-4 w-3 items-center justify-center rounded-sm border bg-border transition-all">
                  <GripVertical className="h-2.5 w-2.5" style={{ color: \`hsl(var(--\${color}-base))\` }} />
               </div>
            </PanelResizeHandle>
            <Panel defaultSize={50} className="flex items-center justify-center p-6">
              <span className="font-semibold" style={{ color: \`hsl(var(--\${color}-base))\` }}>Two</span>
            </Panel>
          </>
        )}
      </PanelGroup>
    </div>
  )
})

Component.displayName = "Resizable-${version}"

export default Component
`;
};

// Generate each version
VERSIONS.forEach(version => {
  const content = TEMPLATE(version);
  const filePath = path.join(TARGET_DIR, `${COMPONENT_NAME}-${version}.tsx`);
  fs.writeFileSync(filePath, content);
  console.log(`Generated ${COMPONENT_NAME}-${version}.tsx`);
});

// Generate index.tsx
const indexContent = `/**
 * Resizable Component - Dynamic Renderer
 * Generated by script
 */

import React, { lazy, Suspense } from 'react';

// Fallback types if file doesn't exist
type ResizableVersion = string;
interface ResizableProps {
  version?: ResizableVersion;
  variant?: 'neutral' | 'primary' | 'success' | 'warning' | 'destructive' | 'info';
  type?: 'default' | 'outline' | 'solid' | 'ghost';
  children?: React.ReactNode;
  direction?: 'horizontal' | 'vertical';
  [key: string]: any;
}

// Config import
let componentConfig: any = { versions: {} };
try {
  // @ts-ignore
  // componentConfig = require('../../config/components/${COMPONENT_NAME}.json');
} catch (e) {
  // console.warn('Config not found for ${COMPONENT_NAME}');
}

// Dynamic imports for versions
const versionComponents: Record<string, any> = {
${VERSIONS.map(v => `  '${v}': lazy(() => import('./${COMPONENT_NAME}-${v}')),`).join('\n')}
};

// Loading Skeleton
const LoadingSkeleton: React.FC = () => (
  <div className="animate-pulse bg-muted/20 w-full h-32 rounded" />
);

// Fallback Component
const FallbackComponent = React.forwardRef<HTMLElement, ResizableProps>(({ 
  children, 
  className = '',
  version,
  ...props 
}, ref) => (
  <div ref={ref as any} className={\`p-4 border border-dashed border-red-500/50 bg-red-500/10 \${className}\`} {...props}>
    <span className="text-xs text-red-400 font-mono">Missing: {version}</span>
    {children}
  </div>
));
FallbackComponent.displayName = 'FallbackResizable';

// Main Component
const ResizableBase = React.forwardRef<HTMLElement, ResizableProps>(({ 
  version = 'angular-corner',
  ...props 
}, ref) => {
  const VersionComponent = versionComponents[version];
  
  if (!VersionComponent) {
    return <FallbackComponent ref={ref} version={version} {...props} />;
  }

  return (
    <Suspense fallback={<LoadingSkeleton />}>
      <VersionComponent ref={ref} {...props} />
    </Suspense>
  );
});
ResizableBase.displayName = 'Resizable';

export const Resizable = Object.assign(ResizableBase, {
  // Subcomponents exports if needed for manual composition
});

export default Resizable;
`;

fs.writeFileSync(path.join(TARGET_DIR, 'index.tsx'), indexContent);
console.log('Generated index.tsx');

// Update main index.ts export
const mainIndexInitPath = path.join(__dirname, '../src/ui/index.ts');
let mainIndexContent = fs.readFileSync(mainIndexInitPath, 'utf8');

// Check if already exported
if (!mainIndexContent.includes("export * from './components/resizable'")) {
    // Add to components section
    const exportStatement = "export * from './components/resizable';";
    // Find a good place to insert (e.g. after navigation-menu)
    if (mainIndexContent.includes("export * from './components/navigation-menu';")) {
        mainIndexContent = mainIndexContent.replace(
            "export * from './components/navigation-menu';",
            "export * from './components/navigation-menu';\n" + exportStatement
        );
    } else {
        // Append to end if specific place not found
        mainIndexContent += `\n${exportStatement}`;
    }
    fs.writeFileSync(mainIndexInitPath, mainIndexContent);
    console.log('Updated src/ui/index.ts with export');
}

console.log('Resizable regeneration complete.');
